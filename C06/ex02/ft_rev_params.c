#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	j;

	while (argc > 1)
	{
		j = 0;
		while (argv[argc - 1][j] != '\0' && argc)
		{
			write(1, &argv[argc - 1][j], 1);
			j++;
		}
		write(1, "\n", 1);
		argc--;
	}
	return (0);
}
