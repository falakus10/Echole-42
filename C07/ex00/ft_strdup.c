#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

char	*ft_strdup(char *src)
{
	int		strlen;
	char	*ptr;
	int		i;

	i = 0;
	strlen = ft_strlen(src);
	ptr = (char *) malloc(strlen * sizeof(char) + 1);
	if (*ptr == ! '\0' )
		return (0);
	while (src[i] != '\0')
	{
		ptr[i] = src[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
